export function getCountriesSorted({ prioritizeBR = true } = {}) {
  const list = Array.isArray(COUNTRIES) ? [...COUNTRIES] : [];
  // remove duplicatas por code
  const map = new Map();
  for (const c of list) {
    if (!c?.code) continue;
    map.set(String(c.code).toUpperCase(), { code: String(c.code).toUpperCase(), name: String(c.name || "").trim() });
  }
  const unique = Array.from(map.values()).filter((x) => x.code && x.name);

  unique.sort((a, b) => a.name.localeCompare(b.name, "pt-BR"));

  if (!prioritizeBR) return unique;

  const br = unique.find((x) => x.code === "BR");
  const rest = unique.filter((x) => x.code !== "BR");
  return br ? [br, ...rest] : unique;
}
